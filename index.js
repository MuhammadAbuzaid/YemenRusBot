bot.command('stats', async (ctx) => { 
    if (ctx.from.id.toString() === ADMIN_ID) { 
        const count = await User.countDocuments(); 
        ctx.reply(`📊 عدد المشتركين: ${count}`); 
    } 
});

bot.command('broadcast', async (ctx) => { 
    if (ctx.from.id.toString() === ADMIN_ID) { 
        const msg = ctx.message.text.replace('/broadcast', '').trim(); 
        if (!msg) return; 
        const users = await User.find(); 
        let successCount = 0;
        for (const u of users) { 
            try { 
                await bot.telegram.sendMessage(u.chatId, msg); 
                successCount++;
                await new Promise(r => setTimeout(r, 100)); // تجنب حظر تيليجرام
            } catch (e) {} 
        } 
        ctx.reply(`✅ تم الإرسال إلى ${successCount} مشترك.`); 
    } 
});

let pendingEdits = {};
bot.action('back_to_menu', (ctx) => { 
    ctx.answerCbQuery(); 
    ctx.reply('القائمة الرئيسية 🏠:', mainMenu); 
});

bot.action(/edit_(.+)/, (ctx) => { 
    pendingEdits[ctx.chat.id] = ctx.match[1]; 
    ctx.answerCbQuery(); 
    ctx.reply('✏️ اكتب الاقتراح الخاص بك لتعديل هذه الإجابة:'); 
});

bot.on('text', (ctx) => { 
    if (pendingEdits[ctx.chat.id]) { 
        bot.telegram.sendMessage(ADMIN_ID, `⚠️ اقتراح من @${ctx.from.username || ctx.from.id}:\n${ctx.message.text}`); 
        ctx.reply('✅ تم إرسال اقتراحك للإدارة، شكراً لك.'); 
        delete pendingEdits[ctx.chat.id]; 
    } 
});

